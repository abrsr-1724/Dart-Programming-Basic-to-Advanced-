  //arithmetic operators
  // Add:+, Subtract: -, Multiply: *, Division: /, Modulus: %

  print(10 + 10);
  print(30 - 10);
  print(10 * 10);
  print(25 / 5);
  print(11 % 3);

  //shorthand, assignment operator

  // =, +=, -=, *=, /=
  int a = 45;
  print(a++ + a++); //46 //91 //100 //92 //90

  // print(a++); //postfix
  // print(++a); //prefix
  // print(a--);
  // print(--a);
  // print(a);

  a+=5; // a = a+5
  a-=5; // a = a-5
  print(a);

  //relational operator

  // >, <, >=, <=, ==, !=

  int x = 40;

  print(x>40); //is it (value of x) greater than 40?
  print(x>=40);
  print(x<40);
  print(x<=40);
  print(x==40);
  print(x!=40);

  //conditional operator
  // && (Logical AND), || (Logical OR)

